package com.example.demo.model;

public class RequestRegistrarEquivalenciaMateria {
    private String institucionOrigenId;
    private String materiaOrigenId;
    private String institucionDestinoId;
    private String materiaDestinoId;

    public String getInstitucionOrigenId() {
        return institucionOrigenId;
    }

    public void setInstitucionOrigenId(String institucionOrigenId) {
        this.institucionOrigenId = institucionOrigenId;
    }

    public String getMateriaOrigenId() {
        return materiaOrigenId;
    }

    public void setMateriaOrigenId(String materiaOrigenId) {
        this.materiaOrigenId = materiaOrigenId;
    }

    public String getInstitucionDestinoId() {
        return institucionDestinoId;
    }

    public void setInstitucionDestinoId(String institucionDestinoId) {
        this.institucionDestinoId = institucionDestinoId;
    }

    public String getMateriaDestinoId() {
        return materiaDestinoId;
    }

    public void setMateriaDestinoId(String materiaDestinoId) {
        this.materiaDestinoId = materiaDestinoId;
    }
}
